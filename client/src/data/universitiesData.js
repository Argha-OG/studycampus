export const getPublicUniversities = () => {
    return malaysianUniversities.filter(uni => uni.type === "public");
};

export const getPrivateUniversities = () => {
    return malaysianUniversities.filter(uni => uni.type === "private");
};

export const getUniversityById = (id) => {
    return malaysianUniversities.find(uni => uni.id === parseInt(id));
};

export const searchUniversities = (query) => {
    const lowerQuery = query.toLowerCase();
    return malaysianUniversities.filter(uni =>
        uni.name.toLowerCase().includes(lowerQuery) ||
        uni.location.toLowerCase().includes(lowerQuery) ||
        uni.programs.some(program => program.title.toLowerCase().includes(lowerQuery))
    );
};

export const filterUniversitiesByType = (type) => {
    if (!type) return malaysianUniversities;
    return malaysianUniversities.filter(uni => uni.type === type);
};
